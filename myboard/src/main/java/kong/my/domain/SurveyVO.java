package kong.my.domain;

public class SurveyVO {
	int s_idx;						//순번
	String s_name;					//이름
	String s_gender;				//성별
	String s_age;					//나이
	String s_email;					//이메일
	String s_purpose_of_visit;		//방문 목적
	String s_level_of_satisfaction;	//만족도
	String s_improvements;			//개선사항
	
	public int getS_idx() {
		return s_idx;
	}
	public void setS_idx(int s_idx) {
		this.s_idx = s_idx;
	}
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_gender() {
		return s_gender;
	}
	public void setS_gender(String s_gender) {
		this.s_gender = s_gender;
	}
	public String getS_age() {
		return s_age;
	}
	public void setS_age(String s_age) {
		this.s_age = s_age;
	}
	public String getS_email() {
		return s_email;
	}
	public void setS_email(String s_email) {
		this.s_email = s_email;
	}
	public String getS_purpose_of_visit() {
		return s_purpose_of_visit;
	}
	public void setS_purpose_of_visit(String s_purpose_of_visit) {
		this.s_purpose_of_visit = s_purpose_of_visit;
	}
	public String getS_level_of_satisfaction() {
		return s_level_of_satisfaction;
	}
	public void setS_level_of_satisfaction(String s_level_of_satisfaction) {
		this.s_level_of_satisfaction = s_level_of_satisfaction;
	}
	public String getS_improvements() {
		return s_improvements;
	}
	public void setS_improvements(String s_improvements) {
		this.s_improvements = s_improvements;
	}
	
	
}
